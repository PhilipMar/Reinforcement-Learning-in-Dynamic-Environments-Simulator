package de.uni.ks.configuration;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotation for fields in {@link Config}. Fields can be ordered by the name attribute of this annotation. This is used
 * by the {@link ConfigManager} and the {@link de.uni.ks.gui.configurator.ConfigurationUI}.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.FIELD)
public @interface Section {

    String name();
}
